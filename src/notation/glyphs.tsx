interface GlyphProps {
  cx: number;
  cy: number;
  size: number;
  className?: string;
}

function Glyph({
  cx,
  cy,
  size,
  className,
  path,
  vb,
  dataGlyph,
}: GlyphProps & {
  path: string;
  vb: { x: number; y: number; w: number; h: number };
  dataGlyph: string;
}) {
  // `size` = target visual height; each glyph scales by its own vb.h
  // so proportions stay intact (Coda's wider-than-tall bbox still
  // fits, Segno's tall S still reaches `size` tall).
  const scale = size / vb.h;
  const gx = cx - (vb.x + vb.w / 2) * scale;
  const gy = cy - (vb.y + vb.h / 2) * scale;
  return (
    <g
      className={className}
      data-glyph={dataGlyph}
      transform={`translate(${gx.toFixed(2)} ${gy.toFixed(2)}) scale(${scale.toFixed(4)})`}
    >
      <path d={path} fill="currentColor" />
    </g>
  );
}

